#!/usr/bin/env node
// report.mjs — aggregate eval/runs/**/metrics.yaml into a per-config scorecard.
// The §19 payoff: "does the scaffolding help?" becomes a table you read, not a
// vibe. Writes eval/results/scorecard.md and scorecard.json.
//
//   node scripts/eval/report.mjs

import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';

const RUNS = 'eval/runs';
const RESULTS = 'eval/results';
const yload = (p) => JSON.parse(execSync(
  `python3 -c "import yaml,json;print(json.dumps(yaml.safe_load(open('${p}')),default=str))"`, { encoding: 'utf8' }));

let order = [];
try { order = (yload('eval/configs.yaml').configs || []).map((c) => c.id); } catch { /* ignore */ }

const runs = [];
if (fs.existsSync(RUNS)) {
  for (const d of fs.readdirSync(RUNS)) {
    const mp = path.join(RUNS, d, 'metrics.yaml');
    if (fs.existsSync(mp)) { try { runs.push(yload(mp)); } catch (e) { console.error(`skip ${d}: ${e.message}`); } }
  }
}
if (!runs.length) { console.error(`no scored runs found under ${RUNS}/ (run eval:score first)`); process.exit(1); }

const byConfig = {};
for (const r of runs) (byConfig[r.config] ||= []).push(r);
const configs = [...new Set([...order.filter((c) => byConfig[c]), ...Object.keys(byConfig)])];

const mean = (xs) => xs.length ? xs.reduce((a, b) => a + b, 0) / xs.length : null;
const r2 = (x) => x == null ? null : Math.round(x * 100) / 100;
const pct = (x) => x == null ? '—' : `${Math.round(x * 100)}%`;
const num = (x) => x == null ? '—' : String(r2(x));

function agg(rs) {
  const acc = rs.map((r) => r.human?.acceptance).filter(Boolean);
  const accPass = acc.filter((a) => a === 'pass').length;
  const validated = rs.map((r) => r.auto?.validate_pass).filter((v) => v === true || v === false);
  const burdens = rs.map((r) => r.human?.review_burden).filter((v) => typeof v === 'number');
  const edits = rs.map((r) => r.human?.manual_edits_after).filter((v) => typeof v === 'number');
  return {
    n: rs.length,
    acceptance_rate: acc.length ? accPass / acc.length : null,
    out_of_scope_avg: mean(rs.map((r) => (r.auto?.out_of_scope || []).length)),
    deletes_total: rs.reduce((a, r) => a + (r.auto?.deletes || []).length, 0),
    forbidden_total: rs.reduce((a, r) => a + (r.auto?.forbidden_ops || []).length, 0),
    changed_lines_avg: mean(rs.map((r) => (r.auto?.insertions || 0) + (r.auto?.deletions || 0))),
    validate_pass_rate: validated.length ? validated.filter(Boolean).length / validated.length : null,
    review_burden_avg: mean(burdens),
    manual_edits_avg: mean(edits),
  };
}

const summary = {};
for (const c of configs) summary[c] = agg(byConfig[c]);

const rows = [
  ['runs (n)', (c) => String(summary[c].n)],
  ['acceptance rate ↑', (c) => pct(summary[c].acceptance_rate)],
  ['out-of-scope / run ↓', (c) => num(summary[c].out_of_scope_avg)],
  ['deletes (total) ↓', (c) => String(summary[c].deletes_total)],
  ['forbidden ops (total) ↓', (c) => String(summary[c].forbidden_total)],
  ['changed lines / run ↓', (c) => num(summary[c].changed_lines_avg)],
  ['validate pass rate ↑', (c) => pct(summary[c].validate_pass_rate)],
  ['review burden 1-5 ↓', (c) => num(summary[c].review_burden_avg)],
  ['manual edits / run ↓', (c) => num(summary[c].manual_edits_avg)],
];
let md = `# §19 Benchmark Scorecard — Madison\n\n`;
md += `_Generated by \`npm run eval:report\` from ${runs.length} scored run(s). ↑ higher is better, ↓ lower is better._\n\n`;
md += `| Metric | ${configs.join(' | ')} |\n|${'---|'.repeat(configs.length + 1)}\n`;
for (const [label, fn] of rows) md += `| ${label} | ${configs.map(fn).join(' | ')} |\n`;
md += `\n## Per-task runs\n\n| Run | Config | Tool | Accept | Out-of-scope | Deletes | Forbidden | Δlines | Validate |\n|---|---|---|---|---|---|---|---|---|\n`;
for (const r of runs.sort((a, b) => (a.task + a.config).localeCompare(b.task + b.config))) {
  md += `| ${r.task} | ${r.config} | ${r.tool} | ${r.human?.acceptance || '—'} | ${(r.auto?.out_of_scope || []).length} | ${(r.auto?.deletes || []).length} | ${(r.auto?.forbidden_ops || []).length} | ${(r.auto?.insertions || 0) + (r.auto?.deletions || 0)} | ${r.auto?.validate_pass == null ? '—' : r.auto.validate_pass} |\n`;
}
md += `\n_Read it as a comparison: if \`full\` does not beat \`baseline\` on out-of-scope, forbidden ops, and acceptance, the scaffolding is not earning its tokens (§19)._\n`;

fs.mkdirSync(RESULTS, { recursive: true });
fs.writeFileSync(path.join(RESULTS, 'scorecard.md'), md);
fs.writeFileSync(path.join(RESULTS, 'scorecard.json'), JSON.stringify({ generated: new Date().toISOString().slice(0, 10), configs: summary, runs }, null, 2) + '\n');
console.log(`wrote ${RESULTS}/scorecard.md and scorecard.json  (${runs.length} runs, configs: ${configs.join(', ')})`);
